function readPath(source, path) {
  return path.split(".").reduce((acc, key) => (acc && Object.prototype.hasOwnProperty.call(acc, key) ? acc[key] : undefined), source);
}

export function detectLocale() {
  return normalizeLocale(window.__TEST_LOCALE__ || navigator.language || "pt-BR");
}

export function createI18n(locale) {
  const resolved = normalizeLocale(locale);
  const dict = dictionaries[resolved] || dictionaries["pt-BR"];
  return {
    locale: resolved,
    t(key, vars = {}) {
      const template = readPath(dict, key) ?? readPath(dictionaries["pt-BR"], key) ?? key;
      if (typeof template !== "string") return template;
      return template.replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? `{${name}}`));
    },
    raw(key) {
      return readPath(dict, key) ?? readPath(dictionaries["pt-BR"], key);
    },
  };
}
